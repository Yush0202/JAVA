public class conditional_elseif {
    public static void main(String args[]){
        int val1=4;
        int val2=4;
        if(val1>val2)
            System.out.println("Value 1 is bigger");
        else if(val1<val2)
            System.out.println("Value 2 is bigger");
        else
            System.out.println("Both Values are equal");
    }
}